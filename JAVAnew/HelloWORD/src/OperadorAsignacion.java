public class OperadorAsignacion {
    public static void main(String[] args)
    {
        int i = 5;
        int j = i +4;


        System.out.println("i = " + i);
        System.out.println("j = " + j);
        
        i += 2;
        System.out.println("i = " + i);

        i += 5;
        System.out.println("i = " + 5);

        j += 4;
        System.out.println("j = " + j);

        j *= 4;
        System.out.println("j = " + j);

    }
}
